package templates

import (
	"html/template"
	"net/http"
	"path/filepath"
	"sync"
)

type templateHandler struct {
	once     sync.Once
	filename string
	templ    *template.Template
}

func NewTemplateHandler(nameOfFile string) *templateHandler {
	return &templateHandler{filename: nameOfFile}
}

// ServeHTTP serves the template with a typical responseWriter and request for a simple web app.
func (t *templateHandler) ServeHTTP(w http.ResponseWriter, r *http.Request) {
	t.once.Do(func() {
		t.templ = template.Must(template.ParseFiles(filepath.Join("templates", t.filename)))
	})
	t.templ.Execute(w, r)
}
